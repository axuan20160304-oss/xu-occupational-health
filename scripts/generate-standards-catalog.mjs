// Main
// ============================================================
const catalog = buildCatalog();

// Stats
const stats = {
  total: catalog.length,
  current: catalog.filter(s => s.status === '现行').length,
  superseded: catalog.filter(s => s.status === '废止').length,
  byCategory: {},
};
for (const cat of CATEGORIES) {
  stats.byCategory[cat] = catalog.filter(s => s.category === cat).length;
}

