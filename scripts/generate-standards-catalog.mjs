function buildCatalog() {
  const catalog = [];
  let id = 0;

  function addEntry(code, title, year, status, catIdx) {
    id++;
    const slug = code.toLowerCase().replace(/[\s\/]+/g, '-').replace(/[.]+/g, '-');
    catalog.push({
      id,
      code,
      title,
      fullTitle: `${code} ${title}`,
      year,
      status: status === 1 ? '现行' : '废止',
      category: CATEGORIES[catIdx] || '其他',
      slug,
      pdfFilename: `${slug}.pdf`,
    });
  }

  // GBZ mandatory
  for (const [code, title, year, status, cat] of GBZ_MANDATORY) {
    addEntry(code, title, year, status, cat);
  }

  // GBZ/T 160 series
  for (const [part, substance] of GBZ_T_160_PARTS) {
    const code = `GBZ/T 160.${part}-2004`;
    const title = `工作场所空气有毒物质测定 ${substance}`;
    addEntry(code, title, 2004, 0, 4); // 已被GBZ/T 300代替
  }

  // GBZ/T 189 series (2007)
  for (const [part, factor] of GBZ_T_189_PARTS) {
    const code = `GBZ/T 189.${part}-2007`;
    const title = `工作场所物理因素测量 第${part}部分：${factor}`;
    addEntry(code, title, 2007, 1, 4);
  }

  // GBZ/T 192 series (2007)
  for (const [part, method] of GBZ_T_192_PARTS) {
    const code = `GBZ/T 192.${part}-2007`;
    const title = `工作场所空气中粉尘测定 第${part}部分：${method}`;
    addEntry(code, title, 2007, 1, 4);
  }

  // GBZ/T 300 series (2017+)
  for (const [part, substance] of GBZ_T_300_PARTS) {
    const code = `GBZ/T 300.${part}-2017`;
    const title = `工作场所空气有毒物质测定 第${part}部分：${substance}`;
    addEntry(code, title, 2017, 1, 4);
  }

  // GBZ/T other
  for (const [code, title, year, status, cat] of GBZ_T_OTHER) {
    addEntry(code, title, year, status, cat);
  }

  // WS standards
  for (const [code, title, year, status, cat] of WS_STANDARDS) {
    addEntry(code, title, year, status, cat);
  }

  return catalog;
}

